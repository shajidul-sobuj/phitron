#include <bits/stdc++.h>
using namespace std;
int main() {
    vector <int> v;                    // size of this vector is zero
    cout << v.size() << endl;
    v.push_back(10);                   // size of this vector is one
    cout << v.size() << endl;
    v.push_back(10);                   // size of this vector is two
    cout << v.size() << endl;
    v.push_back(10);                   // size of this vector is three
    cout << v.size() << endl;
    v.push_back(10);                   // size of this vector is four
    cout << v.size() << endl;

    return 0;
}